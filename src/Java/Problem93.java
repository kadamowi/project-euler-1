	private static boolean isValid(String expression) {
		int h = 0;
		int v = 0;
		int last=Integer.MIN_VALUE;
		for (int i = 0; i < expression.length(); i++) {
			if (expression.charAt(i) == '(') {
				last = i;
				v++;
			}
			else if (expression.charAt(i) == ')') {
				if (last == i-2)
					return false;
				h++;
			} 
			if (h>v) {
				return false;
			}
		}
		
		int paaRad = 0;
		for (int i = 0; i < expression.length(); i++) {
			if (expression.charAt(i) == '(')
				paaRad++;
			else {
				if (paaRad > 1) {
					int paaRad2 = 0;
					boolean f = false;
					for (int j = i; j < expression.length(); j++) {
						if (expression.charAt(j) == ')') {
							f = true;
							paaRad2++;
						} else if (f) 
							break;
					}
					if (paaRad2 >= paaRad)  {
						return false;
					}
				}
				paaRad = 0;
			}
		}
		return h==v;
	}

	private static String addParantheses(String expression) {
		for (int i = 0; i < 3; i++) {
			int index = expression.indexOf((char)('a'+i));
			int para = r.nextInt(4-i);
			String paras = "";
			for (int j = 0; j < para; j++) {
				paras+='(';
			}
			expression = expression.substring(0, index)+paras+expression.substring(index);
		}
		
		for (int i = 1; i < 4; i++) {
			int index = expression.indexOf((char)('a'+i));
			int para = r.nextInt(i+1);
			String paras = "";
			for (int j = 0; j < para; j++) {
				paras+=')';
			}
			expression = expression.substring(0, index+1)+paras+expression.substring(index+1);
		}
		return expression;
	}

